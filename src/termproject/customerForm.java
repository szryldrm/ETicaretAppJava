    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        try {
            boolean anyErr = false;
            if (price > TermProject.customer.getAmount()) {
                alertError("You don't have enough amount");
                anyErr = true;
            }
            if (!anyErr) {
                TermProject.customer.setAmount(TermProject.customer.getAmount() - price);
                Success(Double.toString(price));
                jLabel6.setText(Double.toString(TermProject.customer.getAmount()));
                jLabel10.setText(Double.toString(price));
            }
        } catch (Exception e) {
            alertError("Bir Hata OluÅtu");
        }

 
    }//GEN-LAST:event_jButton2MouseClicked

